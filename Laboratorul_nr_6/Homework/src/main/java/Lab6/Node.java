package Lab6;

/**
 * This class models a node(a stone). A node is defined by where it's located (its row and col)
 */
public class Node {
    private int row;
    private int col;

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    public void setCol(int col) {
        this.col = col;
    }
}
